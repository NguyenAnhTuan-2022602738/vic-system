// Probability distribution data for forecast
export function generateDistributionData(mu: number, sigma: number) {
  const points = []
  for (let x = mu - 4 * sigma; x <= mu + 4 * sigma; x += sigma / 10) {
    const y = (1 / (sigma * Math.sqrt(2 * Math.PI))) * Math.exp(-0.5 * ((x - mu) / sigma) ** 2)
    points.push({ x: parseFloat((x * 100).toFixed(2)), y: parseFloat(y.toFixed(6)) })
  }
  return points
}

